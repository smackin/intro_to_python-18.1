def count_up(start, stop):

    while start <= stop:    #while the start int is less than or equal to the stop int we want to print the start int and then add 1 -  run int through start <= stop - if yes, print start and then add 1,  if no, breaks out.  
        print(start)
        start = start + 1 

count_up(5, 10)        
